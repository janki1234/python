"""In python we can store different data type of value like integer,float,string,boolean and empty"""
#Integer type
i=12
print(f"i is {i}")

#Float type
f=12.4
print(f"f is {f}")

#String type
s="pythonista"
print(f"s is {s}")

#Boolean type
b=True
print(f"b is {b}")

#empty type
n=None
print(f"n is {n}")

